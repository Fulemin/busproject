package com.mashensoft.bus.model;

public class LineUptown {
    private Integer lineUptownId;

    private Integer lineId;

    private Integer uptownId;

    public Integer getLineUptownId() {
        return lineUptownId;
    }

    public void setLineUptownId(Integer lineUptownId) {
        this.lineUptownId = lineUptownId;
    }

    public Integer getLineId() {
        return lineId;
    }

    public void setLineId(Integer lineId) {
        this.lineId = lineId;
    }

    public Integer getUptownId() {
        return uptownId;
    }

    public void setUptownId(Integer uptownId) {
        this.uptownId = uptownId;
    }
}